package model;

public class Car {

    String direction;
    int x;
    int y;

    public Integer getX() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public Integer getY() {
        return this.y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public String getDirection() {
        return this.direction;
    }

    public void setDirection(String direction) {
        this.direction = direction;
    }

}